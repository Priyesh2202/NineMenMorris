package main.java.Utils;

/**
 * @author Priyesh
 *
 * This enum is used to represent the phase of the game.
 *
 * PLACEMENT - The phase where the player places their pieces on the board before moving the pieces.
 * MOVEMENT - The phase where the player moves their pieces on the board.
 * GAMEOVER - The phase where the game is over.
 *
 */
public enum GamePhase {
    PLACEMENT, MOVEMENT, GAMEOVER
}
